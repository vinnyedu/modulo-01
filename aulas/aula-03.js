// Igualdade
// se é igual a ==
// (outra forma) se é igual a ===

console.log(`'10' == 10`, '10' == 10);        // == compara os valores, independente do tipo
console.log(`'10' === 10`, '10' === 10);       // === compara os valores e os tipos

// Diferença
// se é diferente de !=
// (outra forma mais assertiva) se é diferente de !==

console.log(`'10' != 10`, '10' != 10);        // != compara os valores, independente do tipo
console.log(`'10' !== 10`, '10' !== 10);       // !== compara os valores e os tipos

